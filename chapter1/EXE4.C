#include <stdio.h>
/*print fahrentite-celsius table*/
void main()
{      	float celsius, fahr;
	int lower, upper, step;
	lower = 0;
	upper = 10;
	step  = 1;
	celsius  = lower;
	printf("Celsius\t\t\Fahrenheit\n");
	while(celsius <= upper){
		fahr = ((9.0/5.0) * celsius)+32.0;
		printf("%2.0f %6.2f\n",celsius, fahr);
		celsius = celsius+step;
	}
	getch();
	clrscr();
}